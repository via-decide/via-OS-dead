/**
 * scripts/session-compiler.js
 * Session Compiler — tracks the user's spatial traversal and compiles it into a printable PDF report.
 * Uses window.print() which natively invokes "Save as PDF" on Android/iOS.
 */

let sessionLog = [];

// ─── ROOM ENTRY TRACKING ────────────────────────────────────────────────────

// Log the initial boot room on load
document.addEventListener('DOMContentLoaded', () => {
  sessionLog.push({
    title: 'Main Room',
    timestamp: new Date().toLocaleTimeString(),
    coords: '0,0,0'
  });
});

// Track every subsequent room visited
window.addEventListener('os:node_changed', (e) => {
  const { title, x, y, z } = e.detail;
  sessionLog.push({
    title: title || `Node [${x},${y},${z}]`,
    timestamp: new Date().toLocaleTimeString(),
    coords: `${x},${y},${z}`
  });
});

// ─── CONCLUDE BUTTON ────────────────────────────────────────────────────────

document.addEventListener('DOMContentLoaded', () => {
  const concludeBtn = document.getElementById('btn-conclude-session');
  if (concludeBtn) {
    concludeBtn.addEventListener('click', () => compileAndPrint());
  }
});

// ─── PDF RENDER + PRINT ─────────────────────────────────────────────────────

function compileAndPrint() {
  const zone = document.getElementById('pdf-render-zone');
  if (!zone) return;

  const now = new Date();
  const dateStr = now.toLocaleDateString('en-US', {
    weekday: 'long', year: 'numeric', month: 'long', day: 'numeric'
  });
  const timeStr = now.toLocaleTimeString();

  const logItems = sessionLog.map((entry, i) =>
    `<li>
      <span class="log-index">${String(i + 1).padStart(2, '0')}</span>
      <strong>${entry.title}</strong>
      <span class="log-time">&nbsp;—&nbsp;${entry.timestamp}</span>
    </li>`
  ).join('');

  zone.innerHTML = `
    <div class="pdf-page">
      <div class="pdf-header">
        <div class="pdf-logo">VIA OS</div>
        <h1>Session Report</h1>
        <p class="pdf-date">${dateStr} &bull; Session concluded at ${timeStr}</p>
        <hr>
      </div>
      <div class="pdf-body">
        <h2>Spatial Traversal Log</h2>
        <p class="pdf-subtitle">A full record of rooms visited during this session, in order of traversal.</p>
        <ul class="traversal-log">
          ${logItems}
        </ul>
      </div>
      <div class="pdf-footer">
        <p>VIA Spatial OS &bull; Session Nodes Visited: <strong>${sessionLog.length}</strong></p>
        <p>Generated by the VIA OS Session Compiler</p>
      </div>
    </div>
  `;

  zone.style.display = 'block';

  const cleanupAfterPrint = () => {
    zone.style.display = 'none';
    zone.innerHTML = '';

    if (window.OS) {
      window.OS.teleportTo(0, 0, 0);
    }

    sessionLog = [{
      title: 'Main Room',
      timestamp: new Date().toLocaleTimeString(),
      coords: '0,0,0'
    }];
  };

  const handleAfterPrint = () => {
    window.removeEventListener('afterprint', handleAfterPrint);
    cleanupAfterPrint();
  };

  window.addEventListener('afterprint', handleAfterPrint, { once: true });

  requestAnimationFrame(() => {
    requestAnimationFrame(() => {
      setTimeout(() => window.print(), 180);
    });
  });

  setTimeout(() => {
    if (zone.style.display === 'block') cleanupAfterPrint();
  }, 8000);
}
